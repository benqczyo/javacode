package com.benqcz.utils.exception;

public class C3P0UtilsException extends RuntimeException {

	public C3P0UtilsException() {
		super();
	}

	public C3P0UtilsException(String message) {
		super(message);
	}

	public C3P0UtilsException(Throwable cause) {
		super(cause);
	}

	public C3P0UtilsException(String message, Throwable cause) {
		super(message, cause);
	}

}
